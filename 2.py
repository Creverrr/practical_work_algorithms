a=["Чернігівська", "Київська", "Полтавська", "Черкаська", "Дніпропетровська", "Кіровоградська", "Запорізька", "Херсонська"]
n=len(a)
r=n-1
while r>0:
    p=True
    for i in range(r):
        if a[i]>a[i+1]:
            z=a[i]
            a[i]=a[i+1]
            a[i+1]=z
            p=False
    if p:
        break
    r-=1
print(*a)
    